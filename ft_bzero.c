void	bzero(void *s, int n)
{
	int				i;
	unsigned char	*byte_ptr;

	i = 0;
	byte_ptr = (unsigned char *) s;
	while (i < n)
	{
		byte_ptr[i] = 0;
		i++;
	}
}
